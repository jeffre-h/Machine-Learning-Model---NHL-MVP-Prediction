def bargraph(dictionary):
  t.penup()
  t.width(3)
  t.goto(-160,-100)
  t.pendown()  
  t.goto(-160,150)
  t.penup()
  t.goto(-160,-100)
  t.pendown()  
  t.goto(200,-100)
  t.width(1)

  # Insert titles
  t.penup()
  t.goto(20,170)
  t.write('Percent Error on Predictions',None,'center')
  t.goto(-210,0)
  t.write('# of Predictions',None,'center')
  t.goto(20,-140)
  t.write('Percent Error',None,'center')

  # Write y axis values
  t.penup()
  t.goto(-170, -105)
  t.write(0)
  t.goto(-180,140)
  t.write(250)

  x = -152.5
  t.fillcolor('beige')
  for num in dictionary:
    height = dictionary[num]
    t.penup()
    t.goto(x+10,-120)
    t.write("'"+num+"'",None,'center')
    t.goto(x,-100)
    t.pendown()
    t.begin_fill()
    t.goto(x,-100+height)
    t.write(height)
    x = x + 17
    t.goto(x,-100+height)
    t.goto(x,-100)
    t.end_fill()
    t.penup()
    x = x + 15
    t.goto(x,-100)

